#ifndef STRUCTS_H
#define STRUCTS_H

#include <glm/glm.hpp>

/*
 * Структура за версия на OpenGL и GLSL.
 * Съдържа информация за версиите, които ще използва приложението.
 */
struct Version
{
    int gl_major;               // Основна версия на OpenGL (например 4)
    int gl_minor;               // Второстепенна версия на OpenGL (например 6)
    const char* glsl_version;   // Версия на GLSL шейдър езика (например "#version 460")
};

/*
 * Структура за прозореца на приложението.
 * Определя размерите и заглавието на графичния прозорец.
 */
struct Window {
    int window_width;           // Ширина на прозореца в пиксели
    int window_height;          // Височина на прозореца в пиксели
    const char* window_title;   // Заглавие на прозореца
};

/*
 * Структура за камерата в 3D сцената.
 * Дефинира позицията и ориентацията на камерата.
 */
struct Camera {
    glm::vec3 eye;          // Позиция на камерата в световно пространство (X,Y,Z)
    glm::vec3 center;       // Точка, към която гледа камерата (цел)
    glm::vec3 up;           // Вектор, който определя "нагоре" за камерата
};

/*
 * Структура за перспективна проекция.
 * Определя параметрите на 3D проекцията в 2D пространство.
 */
struct Perspective {
    float fov;              // Поле на зрение (Field of View) в радиани
    float aspect;           // Съотношение на страните (ширина/височина)
    float z_near;           // Близка равнина за отсичане (близки обекти)
    float z_far;            // Далечна равнина за отсичане (далечни обекти)
};

/*
 * Структура за робота.
 * Съдържа всички параметри на анимирания 3D робот.
 */
struct Robot {
    // Позиция, ориентация и мащаб
    glm::vec3 position = glm::vec3(0.0f);   // Позиция в световно пространство
    glm::vec3 rotation = glm::vec3(0.0f);   // Ъгли на завъртане (pitch, yaw, roll)
    glm::vec3 scale = glm::vec3(1.0f);      // Мащаб по трите оси

    // Основни части на робота - размери
    glm::vec3 body_size = glm::vec3(0.8f, 1.2f, 0.4f);     // Размер на тялото
    glm::vec3 head_size = glm::vec3(0.5f, 0.5f, 0.5f);     // Размер на главата
    glm::vec3 arm_size = glm::vec3(0.2f, 1.0f, 0.2f);      // Размер на горните части на ръцете
    glm::vec3 leg_size = glm::vec3(0.2f, 1.0f, 0.2f);      // Размер на горните части на краката

    // Сложни части на робота - добавени за по-реалистична анатомия
    glm::vec3 shoulder_size = glm::vec3(0.3f, 0.3f, 0.3f);      // Размер на раменните стави
    glm::vec3 hip_size = glm::vec3(0.4f, 0.3f, 0.4f);           // Размер на ханшовите стави
    glm::vec3 forearm_size = glm::vec3(0.15f, 0.6f, 0.15f);     // Размер на предмишниците
    glm::vec3 shin_size = glm::vec3(0.15f, 0.6f, 0.15f);        // Размер на пищялите
    glm::vec3 eye_size = glm::vec3(0.08f, 0.08f, 0.08f);        // Размер на очите
    glm::vec3 antenna_size = glm::vec3(0.05f, 0.3f, 0.05f);     // Размер на антената

    // Анимационни параметри - контролират движението на частите
    float arm_swing = 0.0f;        // Ъгъл на завъртане на горните части на ръцете
    float leg_swing = 0.0f;        // Ъгъл на завъртане на горните части на краката
    float forearm_swing = 0.0f;    // Ъгъл на завъртане на предмишниците
    float shin_swing = 0.0f;       // Ъгъл на завъртане на пищялите
    float head_bob = 0.0f;         // Вертикално движение на главата
    float antenna_wiggle = 0.0f;   // Завъртане на антената
    float walk_speed = 2.0f;       // Обща скорост на анимацията при движение
};

/*
 * Пространство от имена cg (Computer Graphics).
 * Съдържа глобални инстанции на всички основни структури.
 */
namespace cg {
    // ДЕКЛАРАЦИИ (не дефиниции!) - тези променливи се дефинират в structs.cpp
    extern Version version;             // Версия на OpenGL/GLSL
    extern Window window;               // Параметри на прозореца
    extern Camera camera;               // Параметри на камерата
    extern Perspective perspective;     // Параметри на проекцията
    extern Robot robot;                 // Данни за робота
}

#endif